
class Color
{
	public static red()    { return "rgb(255, 0, 0)";     }
	public static white()  { return "rgb(255, 255, 255)"; }
	public static blue()   { return "rgb(0, 0, 255)";     }
	public static water()  { return "rgba(0, 0, 0, 0)";   }
	public static grey()   { return "rgb(247, 247, 247)"; }
}